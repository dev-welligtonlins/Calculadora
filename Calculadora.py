number_1 = int(input('Entre com primeiro número: '))
number_2 = int(input('Entre com segundo número: '))

print('{} + {} = '.format(number_1, number_2))
print(number_1 + number_2)


#subtração
print('{} - {} = '.format(number_1, number_1))
print(number_1 - number_2)

#Multiplicação
print('{} * {} = '.format(number_1, number_1))
print(number_1 * number_2)

#Divisão
print('{} / {} = '.format(number_1, number_1))
print(number_1 / number_2)